fun main (){
    // todas os tipos de variaveis sao iniciados por letra maiuscula//
    var x: Int = 10 // var
    val y: Int = 12
    var a: Float = 19.76f // numeros decimais e é obrigatorio acrescentar a letra 'f' depois do numero para que o codigo esteja correto;//
    var b: Double = 3.847626859// mais numeros decimais que o float e nao precisa acrescentar o 'f'.
    var c: Long = 3850622985959189099 // menos usado mas abrange quantidade maior de numeros limitado a 19 numeros
    var d: Char = 's' // para letras sempre com ASPAS SIMPLES
    var e: String = "Emilio" // comum em todas as linguagens, sao as frases sempre entre ASPAS DUPLAS
    var f: Short = 199 // pouco utilizado - numeros menores INTEIROS
    var g: Byte = 0b1010// armazena formatos numericos tipos binarios, inicia com 0 seguido da letra 'b' e depois a representação em binario do numero desejado
    var h: Byte = 0x10 // armazena formatos numericos tipos  hexadecimais, inicia com 0 seguido da letra 'x' e depois a representação em hexadecimal do numero desejado
    var i: UInt = 12u //UNSIGNED -  indica o modulo do numero inserido na variavel, numero seguido da letra 'u'.

    println(x)
    println(y)
    println(a)
    println(b)
    println(c)
    println(d)
    println(e)
    println(f)
    println(g)
    println(h)
    println(i)


}